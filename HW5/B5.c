#include <stdio.h>
//Ввести целое число и найти сумму его цифр.
int main() 
{
    int a, sum=0;
    int b;   // переменная для хранения остатка от деления
    scanf("%d", &a);
    
    if (a < 0){
        printf("error");
        return 1;
    }
        while (a > 0) {
        b = a % 10;  // получаем последнюю цифру
        sum += b;         // добавляем её к сумме
        a /= 10;             // отбрасываем последнюю цифру
    }
    
    printf("%d\n", sum);
    return 0;
}
